package com.example.poplartree;



public class StaffRegistrationO {

    public String type;
    public String firstname;
    public String lastname;
    public String email;
    public String userID;

    public StaffRegistrationO(String type, String firstname, String lastname, String email, String userID)
    {
        this.type = type;
        this.firstname = firstname;
        this.lastname = lastname;
        this.email = email;
        this.userID = userID;
    }

}
